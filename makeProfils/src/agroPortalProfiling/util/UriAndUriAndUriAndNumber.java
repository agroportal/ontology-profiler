package agroPortalProfiling.util;

public class UriAndUriAndUriAndNumber {
	
	private String uri1;
	private String uri2;
	private String uri3;
	private Integer number;
	
	public UriAndUriAndUriAndNumber() {
	}

	public UriAndUriAndUriAndNumber(String uri1, String uri2, String uri3, Integer number) {
		this.uri1 = uri1;
		this.uri2 = uri2;
		this.uri3 = uri3;
		this.number = number;
	}

	public String getUri1() {
		return uri1;
	}

	public void setUri1(String uri1) {
		this.uri1 = uri1;
	}

	public String getUri2() {
		return uri2;
	}

	public void setUri2(String uri2) {
		this.uri2 = uri2;
	}

	public String getUri3() {
		return uri3;
	}

	public void setUri3(String uri3) {
		this.uri3 = uri3;
	}

	public Integer getNumber() {
		return number;
	}

	public void setNumber(Integer number) {
		this.number = number;
	}

	
	
}
